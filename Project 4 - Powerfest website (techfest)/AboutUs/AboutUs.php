which commenced in the year 2011 at village Mewad, dist.Mehsana, Gujarat with the approval of All India Council of Technical
Education (AICTE), New Delhi and affiliation with Gujarat Technical University (GTU), Ahmedabad. GPERI offers currently four 
graduate courses (Civil, Computer, Electrical and Mechanical ) in engineering and research opportunities to choose from. 
The institute has intentions to develop state-of-the-art laboratories and infrastructure to cater the need of high skilled
employable engineers to the industry.
